package day02;

/*
变量：程序运行期间，内容可以发生改变的量

创建一个变量并且使用的格式：
    数据类型 变量名称; // 声明，类C语言
    变量名称 = 数据值; // 赋值

    或 数据类型 变量名称 = 数据值; // 声明时初始化
*/

public class Demo01Variable {
    public static void main(String[] args) {
        int a;
        a = 10;
        System.out.println(a);

        byte b;
        b = 20;
        System.out.println(b);

        // 使用long，float类型时一定加后缀


    }
}









