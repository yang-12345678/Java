package cn.itcast.day06.demo03;

public class VariableDifference {
    String name;
    public void methodA() {
        int num = 20;
        System.out.println(num);
    }
}

/*
局部变量和成员变量：
1.定义的位置不一样
局部变量：在方法内部
成员变量：在方法外部，直接写在类当中

2.作用范围不一样
局部变量：只有方法中可以使用，出了方法就不可以用了
成员变量：整个全类都可以用

3.默认值不一样
局部变量：没有默认值，如果要想使用，必须手动赋值
成员变量：如果没有赋值，会有默认值，规则和数组一样

4.内存的位置不一样
局部变量：位于栈内存
成员变量：位于堆内存

5.生命周期不一样
局部变量：随着方法进入栈而生，随着方法出栈而消失
成员变量：随着对象创建而生，随着对象被垃圾回收而消失
*/
